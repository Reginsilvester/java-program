package com.java.org;

public class MethodOverriding {
	public void Empname() {
		System.out.println("Agastin");
		
	}
	public void Empwife() {
		System.out.println("Philomin");
		
	}
	public static void main(String[] args) {
		MethodOverriding m = new MethodOverriding();
m.Empname();	
m.Empwife();
	}

}
